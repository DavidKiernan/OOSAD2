package Week3;

public class Professional {

	protected string name;
	protected string DOB;

	public string getName() {
		return this.name;
	}

	public void setName(string name) {
		this.name = name;
	}

	public string getDOB() {
		return this.DOB;
	}

	public void setDOB(string DOB) {
		this.DOB = DOB;
	}

}